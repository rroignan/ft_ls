char	*ft_strcpy(char *dst, const char *src)
{
	int i;

	i = 0;
	if (dst && src)
	{
		while (src[i] != '\0')
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
		return (dst);
	}
	return (0);
}
