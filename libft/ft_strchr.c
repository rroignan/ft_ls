#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*str;

	str = (char *)s;
	while (*str && *str != (char)c)
	{
		str++;
	}
	if (*str == (char)c)
		return (str);
	return (NULL);
}
